import java.util.Scanner;

class switchcase{
    public static void main(String[] args){
        char var = 's';
        switch(var){

            case 's' :{
                System.out.println("hello");
                System.out.print("saad");
                break;
        }


         case 'm' :{
                System.out.print("how");
                break;

        }
        
         case 'r' :{
                System.out.print("have");
                break;

    }
}
    }
}

